    public override void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        base.OnStateEnter(animator, stateInfo, layerIndex);

        AudioManager.instance.Play(0);

        Cooldown(stateInfo);
    }

    private async void Cooldown(AnimatorStateInfo state)
    {
        if(state.IsName("Slash0"))
            await Task.Delay((int)state.length * 1000);
        else
            await Task.Delay((int)state.length * 1300);
        PlayerMotor.instance.animPlaying = false;
    }

